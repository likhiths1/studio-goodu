// Function to convert title to URL-friendly slug
const toSlug = (str) => {
  return str
    .toLowerCase()
    .replace(/[^\w\s-]/g, '') // Remove special characters
    .replace(/\s+/g, '-')      // Replace spaces with hyphens
    .replace(/--+/g, '-')      // Replace multiple hyphens with single hyphen
    .trim();
};

export const getBlogPost = (idOrSlug) => {
  // First try to find by exact ID
  const postId = String(idOrSlug);
  if (BLOG_POSTS[postId]) {
    return BLOG_POSTS[postId];
  }
  
  // If not found by ID, try to find by slug
  return Object.values(BLOG_POSTS).find(post => 
    toSlug(post.title) === idOrSlug
  ) || null;
};

export const getAdjacentPosts = (currentId) => {
  try {
    const ids = Object.keys(BLOG_POSTS).sort();
    const currentIndex = ids.indexOf(String(currentId));
    
    return {
      prev: currentIndex > 0 ? BLOG_POSTS[ids[currentIndex - 1]] : null,
      next: currentIndex < ids.length - 1 && currentIndex >= 0 ? BLOG_POSTS[ids[currentIndex + 1]] : null
    };
  } catch (error) {
    console.error('Error getting adjacent posts:', error);
    return { prev: null, next: null };
  }
};
